    private void btnSpremiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSpremiActionPerformed
        resetirajGreske();
        if (!kontrola()) {
            return;
        }

        Soba s = new Soba();
        s = napuniObjekt(s);
        obrada.save(s);
        ucitajPodatke();


    }//GEN-LAST:event_btnSpremiActionPerformed

    private void btnPromjenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjenaActionPerformed
        Soba s = lista.getSelectedValue();
        if (s == null) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite stavku");
            return;
        }

        if (txtBrojSobe.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(getRootPane(), "Obavezno broj sobe");
            return;
        }

        resetirajGreske();
        if (!kontrola()) {
            return;
        }
        s = napuniObjekt(s);
        obrada.save(s);
        ucitajPodatke();
    }//GEN-LAST:event_btnPromjenaActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        Soba s = lista.getSelectedValue();
        if (s == null) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite stavku");
            return;
        }
        obrada.delete(s);
        ucitajPodatke();
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void listaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaValueChanged
        if (evt.getValueIsAdjusting()) {
            return;
        }

        Soba s = lista.getSelectedValue();

        if (s == null) {
            return;
        }
        txtBrojSobe.setText(s.getBrojSobe());

        cmbKat.setSelectedItem(s.getKat());
        cmbObroci.setSelectedItem(s.getBrojObroka());
        chInternet.setSelected(true);
        chTelefon.setSelected(true);
        cmbLezaj.setSelectedItem(s.getBrojLezaja());
    }//GEN-LAST:event_listaValueChanged

    private Soba napuniObjekt(Soba s) {

        s.setBrojSobe(txtBrojSobe.getText());

        s.setBrojObroka(Integer.parseInt(cmbObroci.getSelectedItem().toString()));

        s.setBrojLezaja(Integer.parseInt(cmbLezaj.getSelectedItem().toString()));

        s.setKat(Integer.parseInt(cmbKat.getSelectedItem().toString()));

        //s.setInternet(chInternet.get ?????
        //s.setTelefon(chTelefon.get ?????
        //TREBAM LI TRY/CATCH?
        return s;
    }

