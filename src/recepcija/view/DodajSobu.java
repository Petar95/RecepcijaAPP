    private void oznaciGresku(JTextField polje) {
        polje.setBorder(BorderFactory.createLineBorder(Color.decode("#FF0000")));
        polje.requestFocus();
    }

    private boolean kontrola() {
        if (txtBrojSobe.getText().trim().length() == 0) {
            oznaciGresku(txtBrojSobe);
            return false;
        } else {
            return true;
        }
    }

    private void ucitajPodatke() {
        DefaultListModel<Soba> model = new DefaultListModel<>();

        List<Soba> lista = HibernateUtil.getSession().createQuery(
                "from Soba a where a.obrisano=false").list();

        for (Soba s : lista) {
            model.addElement(s);
        }

        this.lista.setModel(model);
    }

    private void resetirajGreske() {
        txtBrojSobe.setBorder(obrub);
    }

